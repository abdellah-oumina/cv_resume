function moveSlide(direction) {
    currentSlide = (currentSlide + direction + slideCount) % slideCount;
    updateCarousel();
}

function goToSlide(index) {
    currentSlide = index;
    updateCarousel();
}

function updateCarousel() {
    const track = document.querySelector('.projects-track');
    const dots = document.querySelectorAll('.carousel-dot');
    
    if (track && dots) {
        track.style.transform = `translateX(-${currentSlide * 100}%)`;
        dots.forEach((dot, i) => {
            dot.classList.toggle('active', i === currentSlide);
        });
    }
}

function showProjectDetails(project) {
    currentImages = project.Screenshots || [];
    currentImageIndex = 0;
    
    const modalOverlay = document.createElement('div');
    modalOverlay.className = 'modal-overlay';
    modalOverlay.innerHTML = `
        <div class="modal-content">
            <div class="modal-header">
                <h2 class="modal-title">${project['Project Name']}</h2>
                <button class="modal-close" onclick="closeModal(this)" aria-label="Close modal">&times;</button>
            </div>
            <div class="modal-body">
                <p>${project.Description}</p>
                <div class="project-tech">
                    ${project.Technology.map(tech => `<span class="tech-tag">${tech}</span>`).join('')}
                </div>
                ${currentImages.length > 0 ? `
                    <div class="project-gallery">
                        ${currentImages.map((img, index) => `
                            <div class="gallery-item" onclick="showImagePreview('${img}', ${index})">
                                <img src="${img}" alt="${project['Project Name']} screenshot ${index + 1}">
                            </div>
                        `).join('')}
                    </div>
                ` : ''}
            </div>
        </div>
    `;
    
    document.body.appendChild(modalOverlay);
    setTimeout(() => modalOverlay.classList.add('active'), 10);
}

function closeModal(button) {
    const modalOverlay = button.closest('.modal-overlay');
    modalOverlay.classList.remove('active');
    setTimeout(() => modalOverlay.remove(), 300);
}
